# -- Functions ------------------------------------------------------------------
def generate_qdt_snippet(_) -> None:
    """Generate QDT snippet for profiles.json files."""
    logger.info("=== START GENERATING QDT SNIPPET ===")

    output_path: Path = Path("./docs/static/_autogenerated/qdt_snippet.json")
    output_path.parent.mkdir(parents=True, exist_ok=True)

    qdt_snippet: dict[str, Union[str, bool, int]] = {
        "name": "QTribu",
        "folder_name": "qtribu",
        "official_repository": True,
        "plugin_id": 2733,
        "version": f"{latest_version}",
    }

    with output_path.open("w", encoding="UTF8") as wf:
        wf.write(json.dumps(qdt_snippet, indent=4, sort_keys=True))


# -- API Doc --------------------------------------------------------
def run_apidoc(_) -> None:
    """Run Sphinx API doc generation."""
    from sphinx.ext.apidoc import main

    logger.info("=== START SPHINX API AUTODOC ===")

    cur_dir: Path = Path(__file__).parent.resolve()
    output_path = str(cur_dir.joinpath("_apidoc").resolve())
    modules = str(cur_dir.joinpath("../qtribu/").resolve())
    exclusions: list[str] = ["../.venv", "../tests", "../CHANGELOG.md"]
    main(["-e", "-f", "-M", "-o", output_path, modules] + exclusions)


# launch setup
def setup(app) -> None:
    """Sphinx setup hook."""
    app.connect("builder-inited", run_apidoc)
    app.connect("builder-inited", generate_qdt_snippet)
